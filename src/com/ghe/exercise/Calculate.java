package com.ghe.exercise;

public class Calculate {  
    public int add(int a, int b){  
        return a+b;  
    }  
    public int minus(int a, int b){  
        return a-b;  
    }  
    public int divide(int a,int b){  
        return a/b;  
    }  
    public int multi(int a, int b){  
        return a*b;  
    }  
}  